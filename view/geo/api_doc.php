<h1>IP Geo Checker API</h1>

<p>
    The api can be reached under the <a href="../geoapi">geoapi/</a> route.
    To pass an ip to the api simply do it in a RESTless fasion by passing
    the ip to the route like this <code>geoapi/ip/:yourIP</code>.
    <br><br>
    Here are a few example links:
