        <li><a href="../geoapi/ip/213.52.129.125">213.52.129.125 (bth.se)</a></li>
        <li><a href="../geoapi/ip/193.11.190.202">193.11.190.202 (bthstudent.se)</a></li>
        <li><a href="../geoapi/ip/194.47.150.9">194.47.150.9 (dbwebb.se)</a></li>
    </ul>
</p>
